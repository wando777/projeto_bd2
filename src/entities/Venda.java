package entities;

public class Venda {

	private int id;
	private int idCorretora;
	private int idMoeda;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getIdCorretora() {
		return idCorretora;
	}

	public void setIdCorretora(int idCorretora) {
		this.idCorretora = idCorretora;
	}

	public int getIdMoeda() {
		return idMoeda;
	}

	public void setIdMoeda(int idMoeda) {
		this.idMoeda = idMoeda;
	}

}
